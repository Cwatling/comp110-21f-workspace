left_side: int = int(input("Enter the left number: "))
right_side: int = int(input("Enter the right number: "))

print(f"{left_side} ** {right_side} is {left_side ** right_side}")
print(f"{left_side} / {right_side} is {left_side / right_side}")
print(f"{left_side} // {right_side} is {left_side // right_side}")
print(f"{left_side} % {right_side} is {left_side % right_side}")